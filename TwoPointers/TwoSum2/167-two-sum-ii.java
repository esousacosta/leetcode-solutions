class Solution {
    public int[] twoSum(int[] numbers, int target) {
        int l = 0;
        int r = numbers.length - 1;

        while (l < r) {
            int sum = numbers[l] + numbers[r];
            if (sum == target) {
                return new int[]{l + 1, r + 1};
            }

            // I reduce the sum by moving r left
            if (sum > target) {
                r--;
            } else {
                // Increase the sum by moving l right.
                l++;
            }
        }

        return null;
    }
}